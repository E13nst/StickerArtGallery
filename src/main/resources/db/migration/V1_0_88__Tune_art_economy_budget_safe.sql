-- Миграция: бюджет-сейф настройка ART-экономики поверх V1_0_87
-- Версия: 1.0.88
-- Контекст:
--   Файл V1_0_87 нельзя менять после применения в БД — Flyway валидирует checksum.
--   Эта миграция приводит данные к целевым значениям «бюджет-сейф» без правки 1.0.87.
-- Описание:
--   Бюджет: 15 000 ₽ AI + 4 000 ₽ infra + $500 Adsgram (~5 000–10 000 новых юзеров)
--   Краны урезаны: активный free user = 15 ART/день → 1 ген за ~7 дней
--   1. Роялти автора пресета: 15 → 10 ART
--   2. Публикация пресета: 300 → 200 ART
--   3. Реферер после первой генерации реферала: 100 → 50 ART
--   4. DAILY_BONUS: 10 → 5 ART
--   5. WELCOME_BONUS: 200 → 50 ART
--   6. swipe_config: 5 ART / 10 свайпов, лимит 20/40

-- ============================================================================
-- 1. Роялти автора пресета
-- ============================================================================
UPDATE art_rules
SET amount      = 10,
    description = 'Начисление автору опубликованного пресета за чужую успешную генерацию (10 ART = 10% от 100 ART)',
    updated_at  = NOW()
WHERE code = 'PRESET_AUTHOR_ROYALTY';

-- ============================================================================
-- 2. Публикация пресета
-- ============================================================================
UPDATE art_rules
SET amount      = 200,
    description = 'Списание 200 ART за публикацию пользовательского пресета (= 2 генерации, окупается при 20+ использованиях)',
    updated_at  = NOW()
WHERE code = 'PUBLISH_PRESET';

-- ============================================================================
-- 3. Реферер
-- ============================================================================
UPDATE art_rules
SET amount      = 50,
    description = 'Бонус рефереру после первой генерации приглашённого (50 ART = 0.5 ген)',
    updated_at  = NOW()
WHERE code = 'REFERRAL_REFERRER_FIRST_GENERATION';

-- ============================================================================
-- 4–5. Ежедневный и приветственный бонус (строки уже созданы в V1_0_87)
-- ============================================================================
UPDATE art_rules
SET amount      = 5,
    description = 'Ежедневное начисление ART за вход в приложение (5 ART; бюджет-сейф: 20 дней = 1 ген)',
    updated_at  = NOW()
WHERE code = 'DAILY_BONUS';

UPDATE art_rules
SET amount      = 50,
    description = 'Приветственный бонус при первом запуске (50 ART = 0.5 ген; мотивирует к действию без полной бесплатной генерации)',
    updated_at  = NOW()
WHERE code = 'WELCOME_BONUS';

-- ============================================================================
-- 6. swipe_config (бюджет-сейф)
-- ============================================================================
UPDATE swipe_config
SET swipes_per_reward     = 10,
    reward_amount         = 5,
    daily_limit_regular   = 20,
    reward_amount_premium = 10,
    daily_limit_premium   = 40,
    updated_at            = NOW()
WHERE id = 1;
